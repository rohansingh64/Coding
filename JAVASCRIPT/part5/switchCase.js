let num = 4;

switch (num) {
  case 1:
    console.log("num is 1");
    break;
  case 2:
    console.log("num is 2");
    break;
  case 3:
    console.log("num is 3");
    break;
  case 4:
    console.log("num is 4");
    break;
  case 5:
    console.log("num is 5");
    break;
  default:
    console.log("num is >= 5");
}
